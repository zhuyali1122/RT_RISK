def get_translations(lang: str) -> dict:
    """Return translation dict for lang; fallback to zh if unknown."""
    return TRANSLATIONS.get(lang, TRANSLATIONS["zh"])


def t(lang: str, key: str, default: str = "") -> str:
    """Translate key for given lang."""
    return get_translations(lang).get(key, default or key)
